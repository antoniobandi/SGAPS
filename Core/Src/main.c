void racunaj_sinus(float amp)
{
	for (int i = 0; i < SAMPLES; i++)
	{
		signal[i] = (amp/U_REF)*(sin(i*2*PI/SAMPLES) + 1) * (0xFFF)/4;
	}
}

void racunaj_pravokutni(float amp)
{
	for (int i = 0; i < SAMPLES; i++)
	{
		if (i < SAMPLES/2)
		{
			signal[i] = (amp/2)*0xFFF/U_REF;
		} else
		{
			signal[i] = 0;
		}
	}
}

